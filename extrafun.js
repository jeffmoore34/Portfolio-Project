function SEmail() {
    var x = document.getElementById('email');
    if (x.style.display == 'none') {
      x.style.display = 'block';
    } else  {
      x.style.display = 'none';
    }
  }
  
  function HideEmail() {
    var x = document.getElementById('email');
    x.style.display = 'none';
    
  }